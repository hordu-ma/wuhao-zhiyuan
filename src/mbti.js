function describeStrength(leftScore, rightScore) {
  const total = leftScore + rightScore;
  if (!total) return "待补充";
  const margin = Math.abs(leftScore - rightScore) / total;
  if (margin >= 0.28) return "明显";
  if (margin >= 0.14) return "中等";
  return "接近均衡";
}

function scoreMbti(answers) {
  const scores = { E: 0, I: 0, S: 0, N: 0, T: 0, F: 0, J: 0, P: 0 };

  questions.forEach((question, index) => {
    const [, , pole] = question;
    const value = Number(answers[index] || 0);
    if (value >= 1 && value <= 5) {
      scores[pole] += value;
    }
  });

  const type = Object.values(dimensions)
    .map(([left, right]) => (scores[left] >= scores[right] ? left : right))
    .join("");
  const preferences = Object.entries(dimensions).map(([dimension, [left, right]]) => {
    const chosen = scores[left] >= scores[right] ? left : right;
    return {
      dimension,
      chosen,
      strength: describeStrength(scores[left], scores[right]),
      scores: { [left]: scores[left], [right]: scores[right] },
      summary: dimensionProfiles[dimension][chosen],
    };
  });

  return {
    type,
    scores,
    preferences,
    summary: typeProfiles[type] || "该类型具有相对清晰的偏好，可结合分数、学科优势和家庭条件进一步分析。",
  };
}

